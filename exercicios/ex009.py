"""
Faça um programa que leia um número inteiro qualquer e mostre na
tela a sua tabuada.
"""

t = int(input('Digite um número inteiro '))

print('\n=======Tabuada do {}======='.format(t))
print('\t{} x {:2} = {}\n'.format(t, 1, t*1))
print('\t{} x {:2} = {}\n'.format(t, 2, t*2))
print('\t{} x {:2} = {}\n'.format(t, 3, t*3))
print('\t{} x {:2} = {}\n'.format(t, 4, t*4))
print('\t{} x {:2} = {}\n'.format(t, 5, t*5))
print('\t{} x {:2} = {}\n'.format(t, 6, t*6))
print('\t{} x {:2} = {}\n'.format(t, 7, t*7))
print('\t{} x {:2} = {}\n'.format(t, 8, t*8))
print('\t{} x {:2} = {}\n'.format(t, 9, t*9))
print('\t{} x {:2} = {}\n'.format(t, 10, t*10))
print('='*27)
